package ru.aafanasiev.util.copier.complex;

/**
 * Целый тип
 *
 * @author aafanasyev
 */
public class Fdouble {
    private double f;

    public Fdouble() {
        super();
    }

    public Fdouble(double f) {
        this();
        this.f = f;
    }

    /**
     * @return the f
     */
    public double getF() {
        return f;
    }

    /**
     * @param f the f to set
     */
    public void setF(double f) {
        this.f = f;
    }
}
