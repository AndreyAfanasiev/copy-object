package ru.aafanasiev.util.copier.complex;

/**
 * Целый тип
 *
 * @author aafanasyev
 */
public class Fbool {
    private boolean f;

    public Fbool() {
        super();
    }

    public Fbool(boolean f) {
        this();
        this.f = f;
    }

    /**
     * @return the f
     */
    public boolean getF() {
        return f;
    }

    /**
     * @param f the f to set
     */
    public void setF(boolean f) {
        this.f = f;
    }
}
