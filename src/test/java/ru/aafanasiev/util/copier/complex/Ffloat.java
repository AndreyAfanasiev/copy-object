package ru.aafanasiev.util.copier.complex;

/**
 * Целый тип
 *
 * @author aafanasyev
 */
public class Ffloat {
    private float f;

    public Ffloat() {
        super();
    }

    public Ffloat(float f) {
        this();
        this.f = f;
    }

    /**
     * @return the f
     */
    public float getF() {
        return f;
    }

    /**
     * @param f the f to set
     */
    public void setF(float f) {
        this.f = f;
    }
}
